    private void btRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRegistrarActionPerformed
        try {

            if (txtNombre.getText().equals("") || txtCorreo.getText().equals("") || txtTel.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Debe ingresar todos los datos.");
            } else {
                String nombre = txtNombre.getText();
                String correo = txtCorreo.getText();
                int telefono = Integer.parseInt(txtTel.getText());

                Connection count = getConnection();
                PreparedStatement var1;
                var1 = count.prepareStatement("INSERT INTO tabla1 (nombre, correo, telefono) values ('" + nombre + "','" + correo + "'," + telefono + ")");

//            String sql = "INSERT INTO bd1 (nombre, correo, telefono) values (?,?,?)";
//            var1 = count.prepareStatement(sql);
//            var1.setString(1, nombre);
//            var1.setString(2, correo);
//            var1.setInt(3, telefono);
                var1.executeUpdate();//Mostrar los datos

                JOptionPane.showMessageDialog(null, "Datos registrados exitosamente");

                count.close();
            }

        } catch (Exception e) {

            System.out.println("error: " + e);
        }
    }//GEN-LAST:event_btRegistrarActionPerformed

